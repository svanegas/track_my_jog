package com.svanegas.trackmyjog.repository.model;

public class User {

    private long id;
    private String name;
    private String email;
    private String role;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }
}
